# LOGICAL OPERATORS
  # and - returns True if both statements are True
  # or - returns True if one of the statements is True
  # not - reverses the result (False if returns true)

# IDENTITY OPERATORS
  # is - returns True if both variables refer to the same object in memory
  # is not - returns True if both variables do not refer to the same object in memory

# MEMBORSHIP OPERATORS
  # in - returns True if a specific value is present within a sequence
  # not in - visa versa

# BITWISE OPERATORS
  # & - AND - Sets each bit to 1 if both bits are 1
  # | - OR - Sets each bit to 1 if one of two bits are 1
  # ^ - XOR - Sets each bit to 1 if only one of two bits are 1
  # ~ - NOT - Inverts all the bits
  # << - Zero fill left shift - Shift left by pushing zeros in from the right and let the left-most bits fall off
  # >> - Zero fill right shift - Shift right by pushing zeros in from the left and let the right-most bits fall off