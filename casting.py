# As an object-oriented language, Python uses classes to define data types, including primitive types

# TYPES OF CONSTRUCTOR FUNCTIONS

int() # constructs an integer number from an integer literal, a float literal, or a string literal (which must be a whole number)
print(int(5))
print(int(5.0505))
print(int('5'))



float() # constructs a float number from an int, float, or str
print(float(1))
print(float(2.8))
print(float("3"))
print(float("3.2"))


str() # constructs a string from a wide variety of data types, including strings, int, and float
print(str('s1'))
print(str(5))
print(str(6.01))