# IF ELSE STATEMENTS

# IF STATEMENT
a = 33
b = 200
if b > a:
  print('b is greater than a')

# ELIF STATEMENT
a = 33
b = 33
if b > a:
  print('b is greater than a')
elif b == a:
  print('a and b are equal')

# ELSE STATEMENT
a = 200
b = 33
if b > a:
  print('b is greater than a')
elif a == b:
  print('a and b are equal')
else:
  print('a is greater than b')

# SHORTHAND IF
